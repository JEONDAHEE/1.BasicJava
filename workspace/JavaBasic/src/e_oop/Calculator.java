package e_oop;

public class Calculator {

	//산술연산
	
	double plus(double a, double b){
		return a + b;
	}
	double minus(double a, double b){
		return a - b;
	}
	double multi(double a, double b){
		return a * b;
	}
	double division(double a, double b){
		return a / b;
	}
	double reminder(double a, double b){
		return a % b;
	}
	
	
	
}
